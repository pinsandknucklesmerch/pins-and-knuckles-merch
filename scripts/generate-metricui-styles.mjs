import { mkdir, readFile, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";

const sourcePath = resolve("node_modules/metricui/dist/styles.css");
const outputPath = resolve("src/features/sales-dashboard/styles/metricui.generated.css");
const header = `/*\n * GENERATED FILE — DO NOT EDIT.\n * Source: node_modules/metricui/dist/styles.css\n * Generated by: npm run generate:metricui-styles\n */\n\n`;

const source = await readFile(sourcePath, "utf8");
const transformed = source
  .replace("@layer theme, base, components, utilities;", "@layer theme, metricui-base, metricui-components, metricui-utilities;")
  .replaceAll("@layer base {", "@layer metricui-base {")
  .replaceAll("@layer components {", "@layer metricui-components {")
  .replaceAll("@layer utilities {", "@layer metricui-utilities {");

if (transformed === source) throw new Error("MetricUI stylesheet did not contain the expected Tailwind cascade layers.");

await mkdir(dirname(outputPath), { recursive: true });
await writeFile(outputPath, `${header}${transformed}`);
